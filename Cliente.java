package DesenvolvimentoNuvem;
public class Cliente{
	
	String nome;
	String cpf;
	String telefone;
	
	
	
	
	
	public Cliente(String nome, String cpf, String telefone) {
		super();
		this.nome = nome;
		this.cpf = cpf;
		this.telefone = telefone;
	}





	public String getNome() {
		return nome;
	}





	public String getCpf() {
		return cpf;
	}





	public String getTelefone() {
		return telefone;
	}


	
	

}
